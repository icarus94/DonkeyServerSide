package server.game.carddeck;

public class Card {
	
	private String sign;
	private int value;
	
	public String getSign() {
		return sign;
	}
	public int getValue() {
		return value;
	}
	public Card(String sign, int value) {
		super();
		this.sign = sign;
		this.value = value;
	}
	
	
	
	

}
